/**
 * @brief  显示单个字符 (8x16 尺寸，带背景色覆盖，零闪烁！)
 */
void LCD_ShowChar(uint16_t x, uint16_t y, char chr, uint16_t fg_color, uint16_t bg_color) {
    uint8_t i, j;
    uint8_t temp;
    uint8_t chr_offset = chr - ' '; /* 算出这个字符在数组里的第几个位置 */

    /* 1. 圈出一个 8宽 x 16高 的专属显存窗口 */
    LCD_SetWindow(x, y, x + 8 - 1, y + 16 - 1);

    /* 2. 扫描这 16 行数据 */
    for (i = 0; i < 16; i++) {
        temp = asc2_1608[chr_offset][i]; /* 取出当前行的数据 (8个像素的0/1状态) */

        for (j = 0; j < 8; j++) {
            /* ⚠️ 极其核心：无论是有字还是没字的地方，都强行用 FSMC 刷颜色！
               这就相当于把“盖掉老数字”和“画新数字”两个动作合二为一，绝对不会闪屏！ */
            if (temp & 0x80) {
                LCD_DATA = fg_color; /* 像素点为 1，画字体颜色 */
            } else {
                LCD_DATA = bg_color; /* 像素点为 0，画背景颜色 */
            }
            temp <<= 1; /* 左移，准备检查下一个像素点 */
        }
    }
}

/**
 * @brief  显示英文字符串
 */
void LCD_ShowString(uint16_t x, uint16_t y, char *str, uint16_t fg_color, uint16_t bg_color) {
    while (*str != '\0') {
        LCD_ShowChar(x, y, *str, fg_color, bg_color);
        x += 8; /* 字符宽度是 8，画完一个光标往右挪 8 个像素 */
        str++;
    }
}

/**
 * @brief  内部算次方函数，为数字显示服务
 */
static uint32_t LCD_Pow(uint8_t m, uint8_t n) {
    uint32_t result = 1;
    while(n--) result *= m;
    return result;
}

/**
 * @brief  显示变量数字 (最适合您的按键计分板！)
 * @param len: 显示几位数 (比如 4 表示显示 0012)
 */
void LCD_ShowNum(uint16_t x, uint16_t y, uint32_t num, uint8_t len, uint16_t fg_color, uint16_t bg_color) {
    uint8_t t, temp;
    uint8_t enshow = 0;

    for (t = 0; t < len; t++) {
        temp = (num / LCD_Pow(10, len - t - 1)) % 10;

        /* 高位去零逻辑，比如把 0012 变成空格 12 */
        if (enshow == 0 && t < (len - 1)) {
            if (temp == 0) {
                LCD_ShowChar(x + 8 * t, y, ' ', fg_color, bg_color);
                continue;
            } else {
                enshow = 1;
            }
        }
        /* 利用 ASCII 码规律：数字 0 的 ASCII 是 48 (也就是 '0') */
        LCD_ShowChar(x + 8 * t, y, temp + '0', fg_color, bg_color);
    }
}






/* T_CS: PD13, T_CLK: PE0, T_MOSI: PE2, T_MISO: PE3, T_IRQ: PE4 */
#define T_CS_LOW()    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, GPIO_PIN_RESET)
#define T_CS_HIGH()   HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, GPIO_PIN_SET)

#define T_CLK_LOW()   HAL_GPIO_WritePin(GPIOE, GPIO_PIN_0, GPIO_PIN_RESET)
#define T_CLK_HIGH()  HAL_GPIO_WritePin(GPIOE, GPIO_PIN_0, GPIO_PIN_SET)

#define T_MOSI_LOW()  HAL_GPIO_WritePin(GPIOE, GPIO_PIN_2, GPIO_PIN_RESET)
#define T_MOSI_HIGH() HAL_GPIO_WritePin(GPIOE, GPIO_PIN_2, GPIO_PIN_SET)

#define T_MISO_READ() HAL_GPIO_ReadPin(GPIOE, GPIO_PIN_3)
#define T_IRQ_READ()  HAL_GPIO_ReadPin(GPIOE, GPIO_PIN_4)

/* 软件模拟 SPI 发送并接收一个字节 */
uint8_t XPT2046_WriteRead(uint8_t data) {
    uint8_t i, res = 0;
    for (i = 0; i < 8; i++) {
        if (data & 0x80) T_MOSI_HIGH(); else T_MOSI_LOW();
        data <<= 1;
        T_CLK_LOW();
        T_CLK_HIGH(); // 上升沿采样
        res <<= 1;
        if (T_MISO_READ()) res |= 0x01;
    }
    return res;
}

/* 读取指定的 ADC 通道（X 或 Y） */
uint16_t XPT2046_Read_Adc(uint8_t cmd) {
    uint16_t res = 0;
    T_CS_LOW();
    XPT2046_WriteRead(cmd);
    // XPT2046 需要一点时间转换，这里读回 16 位，取高 12 位
    res = XPT2046_WriteRead(0x00) << 8;
    res |= XPT2046_WriteRead(0x00);
    T_CS_HIGH();
    return res >> 4; // 12 位有效数据
}



