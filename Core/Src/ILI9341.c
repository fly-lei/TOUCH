/* ========================================================= */
/* 1. 硬件寄存器宏定义 (如果您已经放在 .h 里了，这两行会忽略) */
/* ========================================================= */
#ifndef LCD_REG
#define LCD_REG  (*((volatile uint16_t *)0x60000000)) // RS=0, 发送命令
#define LCD_DATA (*((volatile uint16_t *)0x60020000)) // RS=1, 发送数据
#endif
#define LCD_WIDTH  240
#define LCD_HEIGHT 320

/* ！！！根据您实测数据量身定制的极值参数 ！！！ */
#define XPT_X_MIN  170    /* 左边缘平均值 (194+152)/2 */
#define XPT_X_MAX  1800   /* 右边缘平均值 (1778+1827)/2 */
#define XPT_Y_MIN  120    /* 上边缘平均值 (128+114)/2 */
#define XPT_Y_MAX  1930   /* 下边缘平均值 (1924+1930)/2 */

/* ========================================================= */
/* 2. 最底层的原子函数 (必须放在最前面，防止编译器不认识) */
/* ========================================================= */
void LCD_WriteReg(uint16_t reg) {
    LCD_REG = reg;
}

void LCD_WriteData(uint16_t data) {
    LCD_DATA = data;
}

void LCD_SetWindow(uint16_t xStar, uint16_t yStar, uint16_t xEnd, uint16_t yEnd) {
    LCD_WriteReg(0x2A);
    LCD_WriteData(xStar>>8); LCD_WriteData(xStar&0xFF);
    LCD_WriteData(xEnd>>8);  LCD_WriteData(xEnd&0xFF);

    LCD_WriteReg(0x2B);
    LCD_WriteData(yStar>>8); LCD_WriteData(yStar&0xFF);
    LCD_WriteData(yEnd>>8);  LCD_WriteData(yEnd&0xFF);

    LCD_WriteReg(0x2C); /* 准备接收 FSMC 数据雨！ */
}

/* ========================================================= */
/* 3. 屏幕初始化与大面积刷屏函数 */
/* ========================================================= */
void ILI9341_Init(void) {
    LCD_WriteReg(0x11);
    HAL_Delay(120);
    LCD_WriteReg(0x36);
    LCD_WriteData(0x48); // 竖屏
    LCD_WriteReg(0x3A);
    LCD_WriteData(0x55);
    LCD_WriteReg(0x29);
}

void LCD_Clear(uint16_t color) {
    uint32_t i;
    LCD_SetWindow(0, 0, 239, 319); // 假设是 240x320
    for(i = 0; i < 76800; i++) {
        LCD_DATA = color;
    }
}

void LCD_Fill(uint16_t xsta, uint16_t ysta, uint16_t xend, uint16_t yend, uint16_t color) {
    uint32_t i;
    uint32_t point_count = (xend - xsta + 1) * (yend - ysta + 1);
    LCD_SetWindow(xsta, ysta, xend, yend);
    for(i = 0; i < point_count; i++) {
        LCD_DATA = color;
    }
}

uint16_t Read_ILI9341_ID(void) {
    uint16_t dummy, id_00, id_93, id_41;
    LCD_REG = 0xD3;
    dummy = LCD_DATA;
    id_00 = LCD_DATA;
    id_93 = LCD_DATA;
    id_41 = LCD_DATA;
    return (id_93 << 8) | id_41;
}

